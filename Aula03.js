// 1. Método push()
let frutas = ['maçã', 'banana'];
frutas.push('laranja');
console.log(frutas); // Saída: ['maçã', 'banana', 'laranja']

// 2. Método shift()
let vegetais = ['cenoura', 'batata', 'brócolis'];
vegetais.shift();
console.log(vegetais); // Saída: ['batata', 'brócolis']

// 3. Método sort()
let numeros = [4, 2, 5, 1, 3];
numeros.sort();
console.log(numeros); // Saída: [1, 2, 3, 4, 5]